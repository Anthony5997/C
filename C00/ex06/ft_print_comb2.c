#include <unistd.h>

void	print(int x)
{
	char y;
	char z;

	y = '0' + x / 10;
	z = '0' + x % 10;
	write(1, &y, 1);
	write(1, &z, 1);
}

void	ft_print_comb2(void)
{
	int a;
	int b;

	a = 0;
	while (a <= 99)
	{
		b = a + 1;
		while (b <= 99)
		{
			print(a);
			write(1, " ", 1);
			print(b);
			if (a != 98 || b != 99)
			{
				write(1, ", ", 2);
			}
			b++;
		}
		a++;
	}
}
