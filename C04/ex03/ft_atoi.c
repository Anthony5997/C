int		ft_atoi(char *str)
{
	int nb;
	int i;
	int sign;

	sign = 1;
	i = 0;
	nb = 0;
	while (str[i] == 32 || ((str[i] >= 9 && str[i] <= 13) && str[i] != '\0'))
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			sign = sign * -1;
		}
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb = nb * 10 + (str[i] - 48);
		i++;
	}
	return (sign * nb);
}
